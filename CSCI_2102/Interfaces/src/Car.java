
public class Car {
	public double speed;
	public double getSpeed() {
		return speed;
	}
	public void setSpeed(double speed) {
		this.speed = speed;
	}
	public Car() {
		speed = 0;
	}
	public Car(double velocity) {
		speed = velocity;
	}
	public double getMeasure() {
		return speed;
	}


}
